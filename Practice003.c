// Print the sum of two numbers entered by the user

#include <stdio.h>

int main()
{
int a;
int b;
int sum;
printf("Enter the first number:");
scanf("%d", &a);
{
    printf("Enter the second number:");
    scanf("%d", &b);
}
sum = a+b;
{
    printf("sum of two numbers: %d", sum);
}
return 0;
}